#include "libft.h"

char		*ft_strcat(char *dest, const char *src)
{
	int		a;
	int		i;

	a = 0;
	i = 0;
	while (dest[a] != '\0')
		a++;
	while (src[i] != '\0')
	{
		dest[a] = src[i];
		a++;
		i++;
	}
	dest[a] = '\0';
	return (dest);
}
